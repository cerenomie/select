#include "libft.h"

int		ft_isint(char *s)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (!ft_isdigit(s[i]))
			return (-1);
		i++;
	}
	if (ft_atoi(s))
		return (0);
	else
		return (-1);
}
